package ObjectOriented;

public class ObjectOriented05Constructor {
    /*
     * Constructor（构造方法）专门用来创建对象的方法，通过new创建对象时，实际上就是在调用Constructor
     *       格式：public ClassName (param_type param, ...) {
     *               Method Body
     *           }
     * 可以通过编写适当的Constructor实现对对象成员变量的初始化，但仍然需要getter/setter方法修改成员变量
     * 注意： 1.Constructor的名称必须与类名称完全一致
     *       2.Constructor不需要写return type，也不能有具体的return value
     *       3.如果没有编写Constructor，系统自动补全一个Constructor，没有parameter、method body即public ClassName{}
     *         如果编写至少一个Constructor，编译器将不再补全
     *       4.Constructor也可以进行重载
     * */
}
